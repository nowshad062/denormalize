package com.pvamu.den.model;

public class Anonymization {
	
	private int year ;
	
	private int yearRange ;
	
	private int genderRetain ;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getYearRange() {
		return yearRange;
	}

	public void setYearRange(int yearRange) {
		this.yearRange = yearRange;
	}

	public int getGenderRetain() {
		return genderRetain;
	}

	public void setGenderRetain(int genderRetain) {
		this.genderRetain = genderRetain;
	}
	
			

}
